jQuery(function() {
  var top = $('#callandchat').offset().top - parseFloat($('#callandchat').css('marginTop').replace(/auto/, 0));
  var footTop = $('#footer').offset().top - parseFloat($('#footer').css('marginTop').replace(/auto/, 0));
  var maxY = footTop - $('#callandchat').outerHeight();
  $(window).scroll(function(evt) {
    var y = $(this).scrollTop();
    if (y > top) {
      if (y < maxY) {
        jQuery('#callandchat').addClass('fixed').removeAttr('style');
      } else {
        jQuery('#callandchat').removeClass('fixed').css({
          position: 'absolute',
          top: (maxY - top) + 'px'
        });
      }
    } else {
      jQuery('#callandchat').removeClass('fixed');
    }
  });
});
